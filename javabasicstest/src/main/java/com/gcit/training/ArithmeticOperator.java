package com.gcit.training;

public class ArithmeticOperator {
	
public int add(int exampleVariableOne,int exampleVariableTwo) {
	  
	  
	  int add = exampleVariableOne + exampleVariableTwo;
    return add;  
}
public int sub(int exampleVariableOne,int exampleVariableTwo) {
	int sub = exampleVariableOne - exampleVariableTwo;
	return sub;
}
public int mul(int exampleVariableOne,int exampleVariableTwo) {
	int mul = exampleVariableOne * exampleVariableTwo;
	return mul;
}
public int div(int exampleVariableOne,int exampleVariableTwo) {
	int div = exampleVariableOne / exampleVariableTwo;
	return div;
}
public int mod(int exampleVariableOne,int exampleVariableTwo) {
	int mod = exampleVariableOne % exampleVariableTwo;
	return mod;
}
}