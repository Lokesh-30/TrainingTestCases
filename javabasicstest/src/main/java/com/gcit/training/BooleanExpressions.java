package com.gcit.training;

public class BooleanExpressions {

public boolean check1(int exampleVariableOne,int exampleVariableTwo ) {
    if (exampleVariableOne == exampleVariableTwo) {
    	return true;
    } 
    else {
		return false;
    }    
  }
public boolean check2(int exampleVariableOne,int exampleVariableTwo ) {
	if (exampleVariableOne != exampleVariableTwo) {
		return true;
	} 
	else {
		return false;
	}
}
}